void Display()
{
    int i = 5;

    for(i = 5;i >= 1;i--)
    {
        printf("%d\n",i);
    }
}

////////////////////////////////////////////////////////////////
//Program to display 5 to 1 on Screen
////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}


